namespace RJCP.Core.Terminal.Log
{
    /// <summary>
    /// When logging, indicates the channel that was written to.
    /// </summary>
    public enum ConsoleLogChannel
    {
        /// <summary>
        /// The standard out of the console.
        /// </summary>
        StdOut,

        /// <summary>
        /// The standard error of the console.
        /// </summary>
        StdErr
    }
}
